public class SummaryStats {
    private int wons;
    private int draws;
    private int lost;

    public int getWons() {
        return wons;
    }

    public void setWons(int wons) {
        this.wons = wons;
    }

    public int getDraws() {
        return draws;
    }

    public void setDraws(int draws) {
        this.draws = draws;
    }

    public int getLost() {
        return lost;
    }

    public void setLost(int lost) {
        this.lost = lost;
    }
}
